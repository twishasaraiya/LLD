package model;

public class Package {
    private int size;
    private int weight;
    private int volume;
    private User user;

    public Package() {
    }

    public int getSize() {
        return size;
    }

    public int getWeight() {
        return weight;
    }

    public int getVolume() {
        return volume;
    }

    public User getUser() {
        return user;
    }
}
